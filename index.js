function add(a,b){
  var sum = a + b;
  return sum;
}

function subtract(a,b){
  var difference = a - b;
  return difference;
}

function multiply(a,b){
  var product = a * b;
  return product;
}

function divide(a,b){
  var quotient = a / b;
  return quotient;
}

function inc(n){
  var inc = n + 1;
  return inc;
}

function dec(n){
  var dec = n -1;
  return dec;
}

function makeInt(n){
  var num = parseInt(n,10);
  return num;
}

function preserveDecimal(n){
  var decimalNum = parseFloat(n);
  return decimalNum;
}
